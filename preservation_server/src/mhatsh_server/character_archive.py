def archive_entry_for_character(
    character: PlayableCharacter,
) -> CharacterArchiveEntry | None:
    return CHARACTER_ARCHIVE_BY_MODEL.get(character.model_asset_id)


def archive_entry_for_model(model_asset_id: str) -> CharacterArchiveEntry | None:
    return CHARACTER_ARCHIVE_BY_MODEL.get(str(model_asset_id))

