    model_asset_id: str
    canonical_name: str
    quirk: str
    hero_type: str
    base_rank: str
    skills_captured: str
    passives_captured: str
    talents_captured: str
    completeness: str
    source_ref: str
    role_note: str
    kit_note: str
    data_gaps: tuple[str, ...] = ()

    @property
    def is_high_confidence(self) -> bool:
        return self.completeness.lower() == "high"

    @property
    def has_medium_or_better_kit_coverage(self) -> bool:
        return self.completeness.lower() in {"high", "medium"}


